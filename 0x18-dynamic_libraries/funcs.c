#include <stdlib.h>
/*
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 **/
int add(int a, int b)
{
	return (a + b);
}

/*
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: Remainder of a and b
 **/
int sub(int a, int b)
{
        return (a - b);
}

/*
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: product of a and b
 **/
int mul(int a, int b)
{
        return (a * b);
}

/*
 * mod - modulo two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of a and b
 **/
int mod(int a, int b)
{
        return (a % b);
}

